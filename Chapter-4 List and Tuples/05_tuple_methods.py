# METHODS USED IN TUPLE

t1=(1,4,2,7,3,9,1,2,5,7)
print(t1.count(1))   #counts how many times given value 1 is repeated in the above tuple t1.
print(t1.index(2))    #prints the first index value of 2 even though it is repeated in the tuple.

print(t1.index(9))   #prints the index of the element 9 in the tuple.
