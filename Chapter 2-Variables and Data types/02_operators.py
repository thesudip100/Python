a=15
b=7

#ARITHMETIC OPERATORS(+,-,*,/,%)
print("Sum is ",a+b)
print("Subtraction is ",a-b)
print("Product is ",a*b)
print("Division is ",a/b)
print("Modulo Division is ",a%b)

#ASSIGNMENT OPERATORS(=,+=,-=,*=,/=)
p=43
p+=2
print(p)


#COMPARISION OPERATORS (==,>,<,>=,<=,!=)   => returns booleans
print(a==b) 
print(a>=b)
print(a<=b)
print(14!=7)

#LOGICAL OPERATORS (AND, OR, NOT) ===> operates on booleans
bool1= True
bool2=False
print(bool1 and bool2)
print(bool1 or bool2)
print(not bool1)
